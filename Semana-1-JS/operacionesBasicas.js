// Operaciones Basicas: Comparacion
{
    let a, b;

a == b // false (compara valores, true si se cumple, false si no se cumple)
a === b // false (compara valor y tipo)
a != b // true (si a es diferente de b)
a !== b // true (compara valor y tipo)
a > b //true (Si a es mayor que b)
a < b //false (si a no es menor que b)
a >= b //true (Si a es mayor o igual que b)
a <= b // false (si a no es menor o igual que b)

}

// Operaciones Basicas: Logicas
{
    let a, b;

    a && b // false (AND)
    a || b // true (OR)
    !a // false (NOT)
}

// Operaciones Basicas: Asignacion
{
    let a;

    a += 2;  // c = c + 2 → 7
    a -= 1;  // c = c - 1 → 6
    a *= 3;  // c = c * 3 → 18
    a /= 6;  // c = c / 6 → 3
    a %= 2;  // c = c % 2 → 1
}

// Operaciones Basicas: Incremento y Decremento
{
    let i;

    i++ // Aumenta en 1
    i-- // Disminuye en 1
}
