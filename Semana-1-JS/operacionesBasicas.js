// Operaciones basicas: Aritmeticas
let suma = 1 + 2; // +
let resta = 2 - 1; // -
let multiplicacion = 1 * 2; // *
let division = 10 / 2; // /
let modulo = 9 % 3; //Residuo de 9 / 3 = 1 %
