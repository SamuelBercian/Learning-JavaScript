// VARIABLES EN JAVASCRIPT

// VARIABLES EN JAVASCRIPT: VARIABLE VAR
function variableVar()
{
    var nombre;
    // Esta variable esta disponible en toda la funcion
    // Su uso no es recomendable
} 

// VARIABLES EN JAVASCRIPT: VARIABLE LET
function variableLet()
{
    let nombre = "Samuel";
    // Uso especificamente para valores que pueden variar
    // Esta variable esta disponible en el bloque "{}"
}

// VARIABLES EN JAVASCRIPT: VARIABLE CONST
function variableConst()
{
    const pi = 3.1416;
    // Uso especificamente para valores que no van a cambiar
    // Esta variable esta disponible en el bloque "{}"
}